        $cell.Shading.BackgroundPatternColor = $CLR_HEADER
    }

    # Data rows
    for ($i = 0; $i -lt $rows.Count; $i++) {
        $r = $i + 2
        $row = $rows[$i]
        $isNeg = $negativeIds -contains ($i + 1)
        $rowBg = if ($isNeg) { $CLR_NEG } elseif ($i % 2 -eq 0) { $CLR_WHITE } else { $CLR_ALT }

        # title, precond, steps, testdata, postcond, expected
        $vals = @($row[0], $row[1], $row[2], $row[3], $row[4], $row[5], $row[5], 'Пройден')

        for ($c = 1; $c -le $numCols; $c++) {
            $cell = $tbl.Cell($r, $c)
            $cell.Range.Text = $vals[$c-1]
            $cell.Range.Font.Bold = 0
            $cell.Range.Font.Size = 7
            $cell.Range.Font.Name = 'Times New Roman'
            $cell.Range.Font.Color = $CLR_BLACK
            $cell.Shading.BackgroundPatternColor = $rowBg
            $cell.VerticalAlignment = 0

            # Status column: green bold center
            if ($c -eq $numCols) {
                $cell.Range.Font.Bold = 1
                $cell.Range.Font.Color = $CLR_GREEN
                $cell.Range.Font.Size = 8
                $cell.Range.ParagraphFormat.Alignment = 1
            }
        }
    }

    $tbl.Borders.InsideLineStyle  = 1
    $tbl.Borders.OutsideLineStyle = 1

    # After table
    [void]$sel.EndKey(6)
    $sel.TypeParagraph()
    $sel.Font.Name = 'Times New Roman'; $sel.Font.Size = 11; $sel.Font.Bold = 1
    $sel.Font.Color = $CLR_BLACK; $sel.ParagraphFormat.Alignment = 0
    $sel.TypeText('Итого: 15 тест-кейсов (12 позитивных, 3 негативных). Все тесты пройдены.')

    # === Save ===
    $outPath = 'L:\project\TVCompanyX\lab14_test_cases_react_v2.docx'
    if (Test-Path $outPath) { Remove-Item $outPath -Force }
    $doc.SaveAs([ref]$outPath, [ref]16)
    $doc.Close($false)
    $word.Quit()
    $word = $null
    Write-Host "OK: $outPath"

} catch {
    Write-Error "FAILED: $_"
} finally {
    if ($doc  -ne $null) { try { $doc.Close($false) } catch {} }
    if ($word -ne $null) { try { $word.Quit()        } catch {} }
}
